# 从左向右对比同index元素，直到有不同的时候比较出这两个序列的大小
print((1, 2, 3) < (1, 2, 4))
print([1, 2, 3] < [1, 2, 4])
print((1, 2, 3, 4) < (1, 2, 4))

# 当两个序列不等长的时候，而index相对应的元素都相同时。短序列小于长序列
print((1, 2) < (1, 2, -1))
print((1, 2, 3) == (1.0, 2.0, 3.0))

# 当有字符出现时对比 Unicode
print((1, 2, ('aa', 'ab')) < (1, 2, ('abc', 'a'), 4))

